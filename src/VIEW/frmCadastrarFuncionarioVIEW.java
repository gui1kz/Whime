    private void btnSignInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignInActionPerformed
        String FirstName, LastName, User, Password;
        Long CPF;
        
        FirstName = txtFirstName.getText();
        LastName = txtLastName.getText();
        User = txtUserSign.getText();
        Password = txtPasswordSign.getText();
        CPF = Long.parseLong(txtCPF.getText().replace(".", "").replace("-", ""));

        

        CadastrarFuncionarioDTO objsignindto = new CadastrarFuncionarioDTO();
        objsignindto.setFirstName(FirstName);
        objsignindto.setLastName(LastName);
        objsignindto.setUser(User);
        objsignindto.setPassword(Password);
        objsignindto.setCPF(CPF);

        if (objsignindto.getFirstName() != null && objsignindto.getLastName() != null && objsignindto.getUser() != null && objsignindto.getPassword() != null && objsignindto.getCPF() != null) {
            CadastrarFuncionarioDAO objsignindao = new CadastrarFuncionarioDAO();
            objsignindao.createUser(objsignindto);
            frmLoginVIEW frmloginview = new frmLoginVIEW();
            frmloginview.setVisible(true);

            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Values!");
        }
    }//GEN-LAST:event_btnSignInActionPerformed

